let a = 2;
let b = 3;
console.log('a=', a, ' b=', b);


console.log('Arithmatic Operator');
console.log('a+b=', a + b);
console.log('a-b=', a - b);
console.log('a*b=', a * b);
console.log('a/b=', a / b);
console.log('a%b=', a % b);
console.log('a++=', a++);//Unary Operator
console.log('a--=', a--);//Unary Operator
console.log('a**b=', a ** b); //Power(a^b == a**b)


console.log('Assingement Operator');
let c = 10;
console.log('c=', c);
c += 2;
console.log('c+=2 => c=c+2', c);
c = 10;
c -= 2;
console.log('c-=2 => c=c-2', c);
c = 10;
c *= 2;
console.log('c*=2 => c=c*2', c);
c = 10;
c /= 2;
console.log('c/=2 => c=c/2', c);
c = 10;
c %= 2;
console.log('c%=2 => c=c%2', c);
c = 10;
c **= 2;
console.log('c**=2 => c=c**2', c);
c = 10;

console.log('Comparison Operator');
console.log('a=', a, 'b=', b);
console.log('a==b =>', a == b);
console.log('a===b =>', a === b); //Strictly Equals to
console.log('a!=b =>', a != b);
console.log('a!==b =>', a !== b); //Strictly Not Equals to
console.log('a>b =>', a > b);
console.log('a>=b =>', a >= b);
console.log('a<b =>', a < b);
console.log('a<=b =>', a <= b);

console.log('Logical Operator');
let x = true, y = false;
console.log('true && false = ', x && y);
console.log('true || false = ', x || y);
console.log('!false = ', !y);//Unary Operator


